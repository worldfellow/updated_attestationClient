import { Component } from '@angular/core';
import { Router } from '@angular/router';
import { ApiService } from 'src/app/api.service';
import * as moment from 'moment';
import { ConfirmationService, MessageService } from 'primeng/api';
import { saveAs } from 'file-saver';

interface PageEvent {
  first: number;
  rows: number;
  page: number;
  pageCount: number;
}

@Component({
  selector: 'app-verified-applications',
  templateUrl: './verified-applications.component.html',
  styleUrls: ['./verified-applications.component.css'],
  providers: [ConfirmationService, MessageService],
})
export class VerifiedApplicationsComponent {
  rangeDates: Date[] | undefined;
  verifiedData: any[] = [];
  purposes: any[] = [];
  verifiedLength: any;
  filterText: string = "";
  endDate: any;
  first: number = 0;
  rows: number = 10;
  token: any;
  admin_email: string;
  verifiedShow: boolean = false;
  filepath: any;
  purpose_name: any;
  totalCount: any;

  constructor(
    private router: Router,
    protected api: ApiService,
    private confirmationService: ConfirmationService,
    private messageService: MessageService,
  ) { }

  ngOnInit(): void {


    //get user details from localstorage
    this.token = JSON.parse(localStorage.getItem('user')!);
    this.admin_email = this.token.data.user.user_email;

    //for first time data load calling
    this.refresh(0, 10, " ", " ");

    //get dynamic data of deopdown
    this.api.getPurposeList('').subscribe((data: any) => {
      if (data['data'] != undefined) {
        this.purposes = data['data'];
      }
    });
  }

  filterData() {
    console.log("filterText", this.filterText);
    this.refresh(0, 10, this.filterText, " ")
  }

  filteredByPurpose(purpose_name: any) {
    this.purpose_name = purpose_name;
    console.log('----------', purpose_name);
    this.refresh(0, 10, " ", purpose_name);
  }

  onPageChange(event: PageEvent) {
    this.first = event.first;
    this.rows = event.rows;
    let offset = this.first;
    let limit = this.rows;

    this.filterData();
    let filterData;
    if (!this.filterText) {
      filterData = " ";
    } else {
      filterData = this.filterText;
    }

    this.filteredByPurpose(this.purpose_name);
    let purpose;
    if (!this.purpose_name) {
      purpose = " ";
    } else {
      purpose = this.purpose_name;
    }
    this.refresh(offset, limit, filterData, purpose);
  }

  refresh(offset: number, limit: number, globalSearch: any, purposeSearch: any) {

    this.api.getUserApplication("verified", "accept", " ", offset, limit, " ", " ", globalSearch, purposeSearch).subscribe((data: any) => {
      if (data['status'] == 200) {
        this.verifiedData = data['data'];
        console.log(';;;;;;;;;;;;;;;;;;;;;;;;;', this.verifiedData);

        this.totalCount = data['count']
      } else {
        console.log('Verified data not found!');
      }
    })
  }

  downloadExcel(range: any) {
    const start = moment(range[0]);
    const end = moment(range[1]);

    const startDate = start.format('YYYY-MM-DD');
    const endDate = end.format('YYYY-MM-DD');

    this.api.getDownloadExcel(startDate, endDate, 'verified', "AND tracker='verified'", "AND status='accept'").subscribe((data: any) => {
      if (data['status'] == 200) {
        this.filepath = data['data'];

        this.api.getDownloadBySaveAs(this.filepath).subscribe((data: any) => {
          saveAs(data, 'verifiedApplications.xlsx');
        })
      } else {
        console.log('File path not found!');
      }
    })
  }

  viewMore(user_id: any, app_id: any) {
    this.router.navigate(['pages/adminVerified/viewMore'], { queryParams: { user_id: user_id, app_id: app_id, viewFrom: 'verified' } })
  }

  verify(student: any) {
    this.confirmationService.confirm({
      message: 'Are you sure want to verify application?',
      header: 'Confirmation',
      icon: 'pi pi-exclamation-triangle',

      accept: () => {
        this.api.verifyApplication(student.id).subscribe((data: any) => {
          if (data['status'] == 200) {

          }
        })
      },

      reject: () => {
        this.confirmationService.close();
      }
    })
  }

  sendMultiple() {
    this.confirmationService.confirm({
      message: 'Are you sure want to sign multiple?',
      header: 'Confirmation',
      icon: 'pi pi-exclamation-triangle',

      accept: () => {

      },

      reject: () => {
        this.confirmationService.close();
      }
    })
  }

  rejectApplication(studentUserId: any, studentAppId: any,type:string) {
    this.confirmationService.confirm({
      message: 'Are you sure want to reject application?',
      header: 'Confirmation',
      icon: 'pi pi-exclamation-triangle',

      accept: () => {
        this.api.rejectApplications(studentUserId ,studentAppId,this.admin_email,type).subscribe((data: any) => {
          if (data['status'] == 200) {
            this.ngOnInit();
            this.messageService.add({ severity: 'success', summary: 'Success', detail: data.message });
          } else {
            this.messageService.add({ severity: 'error', summary: 'Error', detail: data.message });
          }
        })
      },

      reject: () => {
        this.confirmationService.close();
      }
    })
  }

  resendApplication(user_id: any, app_id: any,type:string) {
    this.confirmationService.confirm({
      message: 'Are you sure want to resend application?',
      header: 'Confirmation',
      icon: 'pi pi-exclamation-triangle',

      accept: () => {
        this.api.resendApplication(user_id, app_id,type, this.admin_email).subscribe((data: any) => {
          if (data['status'] == 200) {
            this.ngOnInit();
            this.messageService.add({ severity: 'success', summary: 'Success', detail: data['message'] });
          } else {
            this.messageService.add({ severity: 'error', summary: 'Error', detail: data['message'] });
          }
        })
      },

      reject: () => {
        this.confirmationService.close();
      }
    })
  }
}
